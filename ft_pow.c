#include "libft.h"

long int	ft_pow(int a, int b)
{
	long	i;

	i = 1;
	if (b == 0)
		return (1);
	if (b < 0)
		return (0);
	while (0 < b)
	{
		i *= a;
		b--;
	}
	return (i);
}
